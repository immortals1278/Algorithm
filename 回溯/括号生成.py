# 列v所有组合方式可以通过递归来列举

def generateParenthesis(n):
    # 这个函数的作用：生成所有有效的括号组合
    # n 表示有几对括号，比如 n=3 就是 3 对括号

    result = []  # 创建一个空列表，用来存放所有组合

    # 定义一个递归函数（自己调用自己的函数）
    # left: 已经用了多少个左括号 "("
    # right: 已经用了多少个右括号 ")"
    # path: 当前正在构建的括号字符串
    # depth: 当前嵌套深度（连续左括号的数量，用来控制排序）
    def dfs(left, right, path, depth):

        # 如果左括号和右括号都用完了（都用了 n 个）
        if left == n and right == n:
            result.append(path)  # 把当前组合加入结果列表
            return  # 结束这次递归

        # 如果左括号还没用完（还可以加左括号）
        if left < n:
            # 加一个左括号，继续递归
            # left+1：左括号数量+1
            # right：右括号数量不变
            # path+'('：当前字符串后面加一个 '('
            # depth+1：深度+1（因为多了一个左括号，嵌套更深了）
            dfs(left + 1, right, path + '(', depth + 1)

        # 如果右括号数量小于左括号数量（说明可以加右括号）
        if right < left:
            # 加一个右括号，继续递归
            # left：左括号数量不变
            # right+1：右括号数量+1
            # path+')'：当前字符串后面加一个 ')'
            # depth-1：深度-1（因为右括号会结束一层嵌套）
            dfs(left, right + 1, path + ')', depth - 1)

    # 开始递归，初始状态：
    # left=0：还没用左括号
    # right=0：还没用右括号
    # path=''：空字符串
    # depth=0：深度为0
    dfs(0, 0, '', 0)

    # 返回结果列表
    return result


# 程序从这里开始执行
n = int(input())  # 从键盘读取一个数字，比如输入 3

res = generateParenthesis(n)  # 调用函数，得到所有括号组合

# 把结果列表转换成不带引号的格式输出
# ', '.join(res) 的意思是：用逗号加空格把列表中的元素连起来
# 比如 ['((()))', '(()())'] 变成 '((())), (()())'
# 然后加上方括号，变成 [((())), (()())]
print('[' + ', '.join(res) + ']')